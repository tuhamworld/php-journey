<?php
// Comparison Operators are used to compare two values (number or strings)

$x = 56;
$y = 76;

$x == $y; // Equal to
$x === $y; // Strictly Equal to
$x != $y; // Not Equal to
$x !== $y; // Strictly Not Equal to
$x > $y; // Greater than
$x < $y; // Less than
$x >= $y; // Greater than or Equal to
$x <= $y; // Less than or Equal to

?>