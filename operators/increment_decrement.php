<?php
// PHP Increment are used to increment a value
// PHP Decrement are used to decrement a value


$x = 2024;

++$x; // Pre-Increment - Increase the value of x, and then returns the value of x
$x++; // Post-Increment - Returns the value of x, and then Increase the value of x

--$x; // Pre-Decrement - Decrease the value of x, and then returns the value of x
$x--; // Post-Decrement - Returns the value of x, and then decrease the value of x

?>